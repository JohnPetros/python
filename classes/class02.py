# Class 02 - Arithmetic Operators

# Sum -> +
print(5 + 5 + 2)

# Subtraction -> -
print(5 - 3)

# Multiplication -> *
print(5 * 3)

# Division -> /
print(10 / 5)

# Division Quotient -> //
print(5 // 2)

# Division Remainder -> //
print(5 % 2)

# Exponatiation -> **
print(8 ** 2)

#  -> **
print(8 ** 2)